CREATE TABLE IF NOT EXISTS nation  ( N_NATIONKEY INTEGER,
    N_NAME      TEXT,
    N_REGIONKEY  INTEGER,
    N_COMMENT    TEXT);
							
CREATE TABLE IF NOT EXISTS region  ( R_REGIONKEY INTEGER,
    R_NAME       TEXT,
    R_COMMENT   TEXT);
							
CREATE TABLE IF NOT EXISTS part  ( P_PARTKEY INTEGER,
    P_NAME        TEXT,
    P_MFGR        TEXT,
    P_BRAND       TEXT,
    P_TYPE        TEXT,
    P_SIZE        INTEGER,
    P_CONTAINER   TEXT,
    P_RETAILPRICE DECIMAL(15,2),
    P_COMMENT     TEXT);

CREATE TABLE IF NOT EXISTS supplier  ( S_SUPPKEY INTEGER,
    S_NAME        TEXT,
    S_ADDRESS     TEXT,
    S_NATIONKEY   INTEGER,
    S_PHONE       TEXT,
    S_ACCTBAL     DECIMAL(15,2),
    S_COMMENT     TEXT);
							 
CREATE TABLE IF NOT EXISTS partsupp  ( PS_PARTKEY INTEGER,
    PS_SUPPKEY     INTEGER,
    PS_AVAILQTY    INTEGER,
    PS_SUPPLYCOST  DECIMAL(15,2) ,
    PS_COMMENT     TEXT );
						
CREATE TABLE customer  ( C_CUSTKEY INTEGER,
    C_NAME        TEXT,
    C_ADDRESS     TEXT,
    C_NATIONKEY   INTEGER,
    C_PHONE       TEXT,
    C_ACCTBAL     DECIMAL(15,2),
    C_MKTSEGMENT  TEXT,
    C_COMMENT    TEXT);
							 
CREATE TABLE IF NOT EXISTS orders  ( O_ORDERKEY INTEGER,
    O_CUSTKEY        INTEGER,
    O_ORDERSTATUS    TEXT,
    O_TOTALPRICE     DECIMAL(15,2),
    O_ORDERDATE      DATE,
    O_ORDERPRIORITY  TEXT,
    O_CLERK          TEXT,
    O_SHIPPRIORITY   INTEGER,
    O_COMMENT       TEXT);

CREATE TABLE IF NOT EXISTS lineitem ( L_ORDERKEY INTEGER,
    L_PARTKEY     INTEGER,
    L_SUPPKEY     INTEGER,
    L_LINENUMBER  INTEGER,
    L_QUANTITY    DECIMAL(15,2),
    L_EXTENDEDPRICE  DECIMAL(15,2),
    L_DISCOUNT    DECIMAL(15,2),
    L_TAX         DECIMAL(15,2),
    L_RETURNFLAG  TEXT,
    L_LINESTATUS  TEXT,
    L_SHIPDATE    DATE,
    L_COMMITDATE  DATE,
    L_RECEIPTDATE DATE,
    L_SHIPINSTRUCT TEXT,
    L_SHIPMODE    TEXT,
    L_COMMENT      TEXT);
    